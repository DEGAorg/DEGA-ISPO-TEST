export function handleAssignRewards(event: AssignRewardsEvent): void {
  let entity = new AssignRewards(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.sharesToAssignRewards = event.params.sharesToAssignRewards
  entity.totalSharesDeposited = event.params.totalSharesDeposited
  entity.timestamp = event.params.timestamp

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

